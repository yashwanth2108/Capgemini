package com.company;

public class Car_to_Token {
    int carNo;
    int token;

    public Car_to_Token(int carNo, int token) {
        this.carNo = carNo;
        this.token = token;
    }

    public int getCarNo() {
        return carNo;
    }

    public void setCarNo(int carNo) {
        this.carNo = carNo;
    }

    public int getToken() {
        return token;
    }

    public void setToken(int token) {
        this.token = token;
    }
}
